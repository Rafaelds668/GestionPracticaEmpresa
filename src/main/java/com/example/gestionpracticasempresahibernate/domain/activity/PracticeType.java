package com.example.gestionpracticasempresahibernate.domain.activity;

/**
 * Enumerado que representa los tipos de prácticas disponibles.
 */
public enum PracticeType {

    /**
     * Tipo de práctica DUAL.
     * Las prácticas DUAL combinan la formación teórica en el centro educativo con la formación práctica en una empresa.
     */
    /**
     * Tipo de práctica FCT (Formación en Centros de Trabajo).
     * Las prácticas FCT son periodos de formación en empresas u organizaciones que complementan la formación teórica.
     */
    DUAL, FCT
}
